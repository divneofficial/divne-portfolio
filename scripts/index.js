$(function () {
  $(document).scroll(function () {
    var $nav = $(".nav-switch");
    var $navLink1 = $(".nav-link-1");
    var $navLink2 = $(".nav-link-2");
    var $navLink3 = $(".nav-link-3");

    $nav.toggleClass('scrolled', $(this).scrollTop() > $nav.height());
    $navLink1.toggleClass('scrolled', $(this).scrollTop() > $nav.height());
    $navLink2.toggleClass('scrolled', $(this).scrollTop() > $nav.height());
    $navLink3.toggleClass('scrolled', $(this).scrollTop() > $nav.height());
});
});
